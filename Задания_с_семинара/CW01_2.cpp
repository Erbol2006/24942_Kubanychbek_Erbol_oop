/**
 * @brief count_if с лямбдой функцией, второе задание 
 * count_if.
Создать vector содержащий значения типа int (часть значений должна повторяться). Например, {1, 2, 6, 2, 2, 5, 8, 1, 2, 5, 0, 8, 2}.
Создать ещё один vector (также под int) с несколькими элементами, часть из которых встречается в первом векторе. Например, {7, 3, 2, 5}
Посчитать сколько раз встречаются числа из второго вектора в первом с использованием count_if с лямбда-функцией.  
 */
#include <vector>
#include <iostream>
#include <algorithm> // для count_if и find
using namespace std;

int main() {
    //первый вектор — основной набор чисел (с повторами)
    vector<int> v1 = {1, 2, 6, 2, 2, 5, 8, 1, 2, 5, 0, 8, 2};

    //второй вектор — числа, которые мы ищем в первом
    vector<int> v2 = {7, 3, 2, 5};

     // Используем count_if для подсчёта ВСЕХ элементов из v1,
     // которые встречаются в v2.
     // Лямбда-функция получает каждый элемент x из v1 и проверяет,
     // содержится ли он в v2.
    int totalMatches = count_if(
        v1.begin(), 
        v1.end(),
        [&](int x) { // захватываем v2 по ссылке
            // Ищем x в v2
            return find(v2.begin(), v2.end(), x) != v2.end();
        }
    );

    cout << "Общее количество вхождений чисел из второго вектора в первый: "
         << totalMatches << endl;

    // Пояснение:
    // - число 2 встречается в v1 пять раз
    // - число 5 — два раза
    // - числа 7 и 3 — ни разу
    // Итого: 5 + 2 = 7

    return 0;
}